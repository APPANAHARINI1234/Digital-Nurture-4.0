package program_7;

public class Typecast {
    public static void main(String[] args) {
        double d = 3.4;
        //typecast from double to int
        int doubleToInt = (int) d;
        System.out.println(doubleToInt);
        int a = 12;
        //convert to double
        double intToDouble = a;
        System.out.println(intToDouble);
    }
}
