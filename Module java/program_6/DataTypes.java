package program_6;

public class DataTypes {
    public static void main(String[] args) {
        int a = 10;
        byte b = 110;
        char ch = 'a';
        long l = 234L;
        short s = 23;
        boolean flag = true;
        float f = 1.2f;
        double d = 78.89;
        System.out.println("Integer (int): " + a);
        System.out.println("Byte (byte): " + b);
        System.out.println("Character (char): " + ch);
        System.out.println("Long (long): " + l);
        System.out.println("Short (short): " + s);
        System.out.println("Boolean (boolean): " + flag);
        System.out.println("Float (float): " + f);
        System.out.println("Double (double): " + d);
    }
}
